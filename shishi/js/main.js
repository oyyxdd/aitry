// 获取DOM元素
const emojiElement = document.getElementById('appleEmoji');
const quoteElement = document.getElementById('quote');
const refreshBtn = document.getElementById('refreshBtn');

// 修改随机获取函数，确保不会重复
function getRandomItem(array, currentItem) {
    let newItem;
    do {
        newItem = array[Math.floor(Math.random() * array.length)];
    } while (newItem === currentItem && array.length > 1);
    return newItem;
}

// 修改更新内容的函数
function updateContent() {
    // 添加淡出效果
    emojiElement.style.opacity = 0;
    quoteElement.style.opacity = 0;
    
    setTimeout(() => {
        // 获取新的不重复内容
        const newEmoji = getRandomItem(emojis, currentEmoji);
        const newQuote = getRandomItem(quotes, currentQuote);
        
        // 更新当前内容记录
        currentEmoji = newEmoji;
        currentQuote = newQuote;
        
        // 更新显示
        emojiElement.textContent = newEmoji;
        quoteElement.textContent = newQuote;
        
        // 添加淡入效果
        emojiElement.style.opacity = 1;
        quoteElement.style.opacity = 1;
    }, 300);
}

// 初始化页面内容
updateContent();

// 添加刷新按钮点击事件
refreshBtn.addEventListener('click', () => {
    refreshBtn.style.transform = 'scale(0.95)';
    setTimeout(() => {
        refreshBtn.style.transform = 'scale(1)';
    }, 100);
    updateContent();
}); 